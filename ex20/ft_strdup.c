#include <stdlib.h>

int	ft_strlen(char *str)
{
	int	index;

	index = 0;
	while (str[index])
		index++;
	return (index);
}

char	*ft_strcpy(char *dest, char *src)
{
	int	index;

	index = 0;
	while (src[index])
	{
		dest[index] = src[index];
		index++;
	}
	dest[index] = '\0';
	return (dest);
}

char	*ft_strdup(char *src)
{
	char	*duplicate_string;
	int		src_len;

	src_len = ft_strlen(src);
	duplicate_string = (char *)malloc(sizeof(char) * (src_len + 1));
	if (!duplicate_string)
		return (NULL);
	ft_strcpy(duplicate_string, src);
	return (duplicate_string);
}
